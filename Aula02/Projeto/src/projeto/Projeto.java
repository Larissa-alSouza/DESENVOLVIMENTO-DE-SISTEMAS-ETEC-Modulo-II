package projeto;

public class Projeto {

    public static void main(String[] args) {
        
        int x;
        x = 1;
        
        switch(x)
        {
            case 0:
                System.out.println("x é 0");
                break;
            case 1:
                System.out.println("x é 1");
                break;
            default:
                System.out.println("x tem um valor diferente de 0 e 1");
                break;
        }
    }
    
}
